def write_inventory_csv(artifacts: list[Artifact], csv_path: Path) -> None:
    with csv_path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["id", "type", "title", "path", "source", "modified", "summary"])
        for artifact in artifacts:
            writer.writerow([artifact.id, artifact.type, artifact.title, artifact.path, artifact.source, artifact.modified, artifact.summary])


def _kv_lines(data: object) -> list[str]:
    if not isinstance(data, dict) or not data:
        return ["- none"]
    return [f"- `{key}`: `{value}`" for key, value in sorted(data.items())]


def _md(text: object) -> str:
    return str(text).replace("|", "\\|").replace("\n", " ").strip()


def _source_href(path: str, line: int = 0) -> str:
    suffix = f"&line={line}#L{line}" if line else ""
    return f"/governance/source?path={quote(path)}{suffix}"
