def write_outputs(result: AuditResult, out_dir: Path) -> dict[str, Path]:
    out_dir.mkdir(parents=True, exist_ok=True)
    registry = out_dir / "registry.json"
    report = out_dir / "report.md"
    dashboard = out_dir / "dashboard.html"
    csv_path = out_dir / "inventory.csv"
    agent_context = out_dir / "agent-context-current.md"

    data = result.to_dict()
    data["workpacks"] = _workpacks(result)
    registry.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    report.write_text(render_markdown(result), encoding="utf-8")
    dashboard.write_text(render_dashboard(result, out_dir), encoding="utf-8")
    write_inventory_csv(result.artifacts, csv_path)
    agent_context.write_text(render_agent_context(result, data), encoding="utf-8")
    return {
        "registry": registry,
        "report": report,
        "dashboard": dashboard,
        "inventory_csv": csv_path,
        "agent_context": agent_context,
    }


def render_markdown(result: AuditResult) -> str:
    lines: list[str] = []
    stats = result.stats
    lines.append("# Skill / Workflow Governance Report")
    lines.append("")
    lines.append(f"- Generated: `{result.generated_at}`")
    lines.append(f"- Root: `{result.root}`")
    lines.append(f"- Artifacts: `{stats['artifact_count']}`")
    lines.append(f"- Issues: `{stats['issue_count']}`")
    lines.append("")

    lines.append("## Summary")
    lines.append("")
    lines.append("### Artifacts By Type")
    lines.append("")
    lines.extend(_kv_lines(stats.get("by_type", {})))
    lines.append("")
    lines.append("### Issues By Severity")
    lines.append("")
    lines.extend(_kv_lines(stats.get("by_severity", {})))
    lines.append("")

    lines.append("## Issues")
    lines.append("")
    if not result.issues:
        lines.append("No issues found.")
    else:
        lines.append("| Severity | Category | Artifact | Evidence | Suggestion |")
        lines.append("|---|---|---|---|---|")
        for issue in result.issues:
            loc = f"{issue.path}:{issue.line}" if issue.line else issue.path
            lines.append(
                "| "
                + " | ".join(
                    [
                        _md(issue.severity),
                        _md(issue.category),
                        _md(loc),
                        _md(issue.evidence),
                        _md(issue.suggestion),
                    ]
                )
                + " |"
            )
    lines.append("")

    lines.append("## Inventory")
    lines.append("")
    lines.append("| Type | ID | Title | Path | Source |")
    lines.append("|---|---|---|---|---|")
    for artifact in result.artifacts:
        lines.append(
            "| "
            + " | ".join(
                [
                    _md(artifact.type),
                    _md(artifact.id),
                    _md(artifact.title),
                    _md(artifact.path),
                    _md(artifact.source),
                ]
            )
            + " |"
        )
    lines.append("")
    return "\n".join(lines)


def render_agent_context(result: AuditResult, registry: dict[str, object]) -> str:
    """A portable context pack that Codex/Claude can read without the dashboard UI."""
    root = Path(result.root).resolve()
    hub = compact_hub_for_prompt(build_governance_hub(root, registry))
    stats = result.stats
    lines: list[str] = [
        "# GameDraft Governance Agent Context",
        "",
        "This file is generated by `python3 -B tools/skill_workflow_governance/govern.py audit`.",
        "Codex, Claude, or another agent client can reference this file directly instead of reading the dashboard page.",
        "",
        "## Client Contract",
        "",
        "- Treat the dashboard as a view only; the canonical machine-readable state is `registry.json` and the `governance://` resources.",
        "- For read-only analysis, do not modify files. For fixes, only edit the files implicated by selected workpacks or evidence.",
        "- After any fix, rerun `python3 -B tools/skill_workflow_governance/govern.py audit` and report remaining risks.",
        "- Do not invent host tools. If an MCP client is connected, use the tools/resources listed below; otherwise read the files named here.",
        "",
        "## Direct Files",
        "",
        "- `tools/skill_workflow_governance/out/registry.json`: full structured audit state.",
        "- `tools/skill_workflow_governance/out/report.md`: human-readable audit report.",
        "- `tools/skill_workflow_governance/out/dashboard.html`: local visual dashboard.",
        "- `tools/skill_workflow_governance/out/agent-context-current.md`: this portable agent context.",
        "",
        "## MCP Server",
        "",
        "```json",
        json.dumps(
            {
                "mcpServers": {
                    "gamedraft-governance": {
                        "command": "python3",
                        "args": [
                            "-B",
                            "tools/skill_workflow_governance/mcp_server.py",
                            str(root),
                        ],
                    }
                }
            },
            ensure_ascii=False,
            indent=2,
        ),
        "```",
        "",
        "## Audit Summary",
        "",
        f"- Generated: `{result.generated_at}`",
        f"- Root: `{result.root}`",
        f"- Artifacts: `{stats.get('artifact_count', 0)}`",
        f"- Issues: `{stats.get('issue_count', 0)}`",
        f"- By severity: `{json.dumps(stats.get('by_severity', {}), ensure_ascii=False)}`",
        f"- By category: `{json.dumps(stats.get('by_category', {}), ensure_ascii=False)}`",
        "",
        "## Governance Resources",
        "",
    ]

    resources = hub.get("resources") if isinstance(hub.get("resources"), list) else []
    for resource in resources:
        if not isinstance(resource, dict):
            continue
        uri = str(resource.get("uri") or "")
        title = str(resource.get("title") or uri)
        summary = str(resource.get("summary") or "")
        count = resource.get("count")
        count_text = f" ({count})" if count is not None else ""
        lines.append(f"- `{uri}`{count_text}: {title}。{summary}")

    lines.extend(["", "## Governance Tools", ""])
    tools = hub.get("tools") if isinstance(hub.get("tools"), list) else []
    for tool in tools:
        if not isinstance(tool, dict):
            continue
        name = str(tool.get("name") or "")
        side_effect = str(tool.get("sideEffect") or "")
        approval = "requires approval" if tool.get("requiresApproval") else "no approval"
        desc = str(tool.get("description") or tool.get("title") or "")
        lines.append(f"- `{name}` [{side_effect}, {approval}]: {desc}")

    lines.extend(["", "## Workpacks", ""])
    workpacks = registry.get("workpacks") if isinstance(registry.get("workpacks"), list) else []
    if not workpacks:
        lines.append("No workpacks found.")
    for pack in workpacks:
        if not isinstance(pack, dict):
            continue
        lines.extend(
            [
                f"### {pack.get('priority', 'P?')} {pack.get('title', pack.get('id', 'workpack'))}",
                "",
                f"- URI: `governance://workpack/{pack.get('id', '')}`",
                f"- Kind: `{pack.get('kind', '')}`",
                f"- Issues: `{pack.get('issue_count', 0)}`",
                f"- Summary: {pack.get('summary', '')}",
                f"- Next: {pack.get('next', '')}",
            ]
        )
        paths = pack.get("paths") if isinstance(pack.get("paths"), list) else []
        if paths:
            lines.append("- Paths:")
            for path in paths[:12]:
                lines.append(f"  - `{path}`")
        prompt = str(pack.get("agent_prompt") or "").strip()
        if prompt:
            lines.extend(["", "Prompt:", "", "```text", prompt, "```"])
        lines.append("")

    lines.extend(
        [
            "## Compact Host Snapshot",
            "",
            "```json",
            json.dumps(hub, ensure_ascii=False, indent=2),
            "```",
            "",
        ]
    )
    return "\n".join(lines)


def render_dashboard(result: AuditResult, out_dir: Path) -> str:
    data = result.to_dict()
    data["workpacks"] = _workpacks(result)
    data["mcp"] = _mcp_client_config(Path(result.root).resolve())
    for artifact in data["artifacts"]:
        artifact["href"] = _source_href(artifact["path"])
    for issue in data["issues"]:
        issue["href"] = _source_href(issue["path"], issue.get("line") or 0)
    payload = json.dumps(data, ensure_ascii=False).replace("</", "<\\/")
    template = DASHBOARD_HTML.replace("{{", "{").replace("}}", "}")
    return template.replace("%DATA_JSON%", payload)


def _mcp_client_config(root: Path) -> dict[str, object]:
    python = root / ".tools" / "venv" / "bin" / "python"
    python_command = str(python) if python.exists() else "python3"
    launcher = root / "tools" / "skill_workflow_governance" / "mcp_server.py"
    stdio_command = [python_command, "-B", str(launcher), str(root)]
    return {
        "serverName": "gamedraft-governance",
        "root": str(root),
        "command": python_command,
        "args": ["-B", str(launcher), str(root)],
        "stdioCommand": stdio_command,
        "codexInstall": _shell_join(
            [
                "/Applications/Codex.app/Contents/Resources/codex",
                "mcp",
                "add",
                "gamedraft-governance",
                "--",
                *stdio_command,
            ]
        ),
        "claudeCodeInstall": _shell_join(
            [
                "claude",
                "mcp",
                "add",
                "--scope",
                "user",
                "gamedraft-governance",
                "--",
                *stdio_command,
            ]
        ),
        "claudeDesktopConfig": {
            "mcpServers": {
                "gamedraft-governance": {
                    "command": python_command,
                    "args": ["-B", str(launcher), str(root)],
                }
            }
        },
    }


def _shell_join(parts: list[str]) -> str:
    def quote(part: str) -> str:
        if not part:
            return "''"
        safe = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_+-=./:"
        if all(char in safe for char in part):
            return part
        return "'" + part.replace("'", "'\"'\"'") + "'"

    return " ".join(quote(str(part)) for part in parts)


def _workpacks(result: AuditResult) -> list[dict[str, object]]:
    issues_by_category: dict[str, list[Issue]] = defaultdict(list)
    for issue in result.issues:
        issues_by_category[issue.category].append(issue)

    artifact_by_id = {artifact.id: artifact for artifact in result.artifacts}
    config = {
        "broken-reference": {
            "priority": "P0",
            "title": "断链修复包",
            "kind": "agent-fix",
            "summary": "缺失引用集中处理，不要逐条手改。按文件分组检查改名、移动、删除三种情况。",
            "next": "交给 agent 执行：能确定的路径直接修；明显过期的引用删；无法判断的只列成确认清单。",
        },
        "drift-risk": {
            "priority": "P1",
            "title": "规则漂移复核包",
            "kind": "agent-review",
            "summary": "规则/工作流引用的代码或文档更新过，说明文字可能已经落后。",
            "next": "交给 agent 对比规则和当前实现：过时就改规则，仍然正确就标记已验证。",
        },
        "missing-metadata": {
            "priority": "P1",
            "title": "Skill 触发条件补齐包",
            "kind": "agent-edit",
            "summary": "Skill 没写清什么时候该用，容易让 Codex 和 Claude 误触发或漏触发。",
            "next": "交给 agent 批量补 when-to-use / when-not-to-use，不改变 skill 正文语义。",
        },
        "missing-lifecycle": {
            "priority": "P2",
            "title": "生命周期元数据补齐包",
            "kind": "batch-edit",
            "summary": "缺少 status / owner / last verified 这类治理字段，可以标准化批量补。",
            "next": "交给 agent 批量插入统一生命周期块，默认 owner 为 shared，验证日期用本次审计日期。",
        },
        "stale-candidate": {
            "priority": "P2",
            "title": "疑似过期归档包",
            "kind": "agent-review",
            "summary": "长期没变的规则或工作流不一定错，但需要确认还在不在用。",
            "next": "交给 agent 查引用和最近使用痕迹：仍在用则补验证记录，不再用则建议归档。",
        },
        "weak-workflow-entry": {
            "priority": "P2",
            "title": "工作流入口补齐包",
            "kind": "agent-edit",
            "summary": "工作流文档没有明显脚本、工具或检查入口，后续 agent 很难执行。",
            "next": "交给 agent 给每个工作流补入口链接、命令或标记为背景资料。",
        },
        "possible-overlap": {
            "priority": "P3",
            "title": "Skill 重叠边界包",
            "kind": "agent-review",
            "summary": "相似 skill 可能职责重叠，先确认边界，再决定合并、拆分或互链。",
            "next": "交给 agent 比较触发条件，只输出边界建议和候选改法，不直接大改。",
        },
    }

    priority_order = {"P0": 0, "P1": 1, "P2": 2, "P3": 3}
    packs: list[dict[str, object]] = []
    for category, issues in issues_by_category.items():
        meta = config.get(
            category,
            {
                "priority": "P3",
                "title": category,
                "kind": "review",
                "summary": "未分类治理项，先作为证据保留。",
                "next": "交给 agent 归类后再决定是否处理。",
            },
        )
        affected = sorted({issue.path for issue in issues})
        issue_ids = [issue.id for issue in issues]
        artifact_types = sorted(
            {
                artifact_by_id[issue.artifact_id].type
                for issue in issues
                if issue.artifact_id in artifact_by_id
            }
        )
        prompt = _agent_prompt(
            title=str(meta["title"]),
            category=category,
            summary=str(meta["summary"]),
            next_action=str(meta["next"]),
            paths=affected,
            issue_count=len(issues),
        )
        packs.append(
            {
                "id": category,
                "priority": meta["priority"],
                "title": meta["title"],
                "kind": meta["kind"],
                "summary": meta["summary"],
                "next": meta["next"],
                "issue_count": len(issues),
                "artifact_count": len(affected),
                "artifact_types": artifact_types,
                "paths": affected[:12],
                "hidden_path_count": max(0, len(affected) - 12),
                "issue_ids": issue_ids,
                "sample_issues": [_issue_sample(issue) for issue in issues[:6]],
                "agent_prompt": prompt,
            }
        )
    return sorted(packs, key=lambda pack: (priority_order.get(str(pack["priority"]), 9), -int(pack["issue_count"]), str(pack["title"])))


def _issue_sample(issue: Issue) -> dict[str, object]:
    return {
        "id": issue.id,
        "severity": issue.severity,
        "path": issue.path,
        "line": issue.line,
        "title": issue.title,
        "evidence": issue.evidence,
        "href": _source_href(issue.path, issue.line),
    }


def _agent_prompt(
    *,
    title: str,
    category: str,
    summary: str,
    next_action: str,
    paths: list[str],
    issue_count: int,
) -> str:
    shown = "\n".join(f"- {path}" for path in paths[:20])
    more = f"\n- ...还有 {len(paths) - 20} 个文件，详见 dashboard/registry.json" if len(paths) > 20 else ""
    return (
        f"请处理 GameDraft Skill/Workflow 治理包：{title}（{category}，{issue_count} 项）。\n"
        f"目标：{summary}\n"
        f"执行方式：{next_action}\n"
        "要求：不要逐条问用户；先按文件分组，能自动修的直接修，无法判断的最后汇总成不超过 5 条确认项；完成后运行 "
        "`python3 -B tools/skill_workflow_governance/govern.py audit` 验证。\n"
        f"涉及文件：\n{shown}{more}"
    )


